import { semver } from "./semver";

describe("Inspect semver function", () => {
  it("semver is a function", () => {
    expect(semver).toBeInstanceOf(Function);
  });

  it(`['5.5.1', '4.21.0', '4.22.0', '6.1.0', '5.1.0', '4.5.0'] => ['4.5.0','4.21.0','4.22.0', '5.1.0', '5.5.1',,'6.1.0']`, () => {
    const input = ["5.5.1", "4.21.0", "4.22.0", "6.1.0", "5.1.0", "4.5.0"];
    expect(semver(input)).toEqual([
      "4.5.0",
      "4.21.0",
      "4.22.0",
      "5.1.0",
      "5.5.1",
      "6.1.0",
    ]);
  });

  it(`["1.0.5", "2.5.0", "0.12.0", "1", "1.23.45", "1.4.50", "1.2.3.4.5.6.7"] => [ "0.12.0", "1", "1.0.5", "1.2.3.4.5.6.7", "1.4.50", "1.23.45", "2.5.0" ]`, () => {
    const input = [
      "1.0.5",
      "2.5.0",
      "0.12.0",
      "1",
      "1.23.45",
      "1.4.50",
      "1.2.3.4.5.6.7",
    ];
    expect(semver(input)).toEqual([
      "0.12.0",
      "1",
      "1.0.5",
      "1.2.3.4.5.6.7",
      "1.4.50",
      "1.23.45",
      "2.5.0",
    ]);
  });
});
